import { useEffect, useMemo } from 'react';
import createBattleInputController from '../input/BattleInputController';

export default function useBattleSceneInputController({
  open = false,
  glCanvasRef,
  runtimeRef,
  cameraRef,
  cameraViewRectRef,
  worldToScreenRef,
  pointerWorldRef,
  panDragRef,
  deployYawDragRef,
  deployRectDragRef,
  spacePressedRef,
  selectedSquadId = '',
  battleUiMode = '',
  skillConfirmState = null,
  aimState = null,
  deployDraggingGroupId = '',
  deployDraggingTeam = 'attacker',
  selectedPaletteItemId = '',
  isTrainingMode = false,
  resolveDeployPlacementTeam,
  switchDeployGroupTeamForTraining,
  isPathPointBlocked,
  syncBattleCards,
  selectBattleSquad,
  closeSkillConfirm,
  closeSkillPick,
  closeMarchModePick,
  recallDeployDraggingGroup,
  setClockPaused,
  setCards,
  setMinimapSnapshot,
  setIsPanning,
  setDeployNotice,
  setSelectedSquadId,
  setDeployDraggingGroup,
  setDeployActionAnchorMode,
  setPendingPathPoints,
  setPlanningHoverPoint,
  setBattleUiMode,
  setSkillPopupSquadId,
  setAimState,
  setSkillConfirmState,
  setWorldActionsVisibleForSquadId,
  ORDER_MOVE,
  CAMERA_ZOOM_STEP,
  CAMERA_DISTANCE_MIN,
  CAMERA_DISTANCE_MAX,
  DEPLOY_ROTATE_SENSITIVITY,
  DEPLOY_ROTATE_CLICK_THRESHOLD,
  DEPLOY_PITCH_DEG,
  BATTLE_UI_MODE_NONE,
  BATTLE_UI_MODE_PATH,
  BATTLE_UI_MODE_MARCH_PICK,
  BATTLE_UI_MODE_SKILL_PICK,
  BATTLE_UI_MODE_SKILL_CONFIRM,
  skillRangeByClass,
  skillAoeRadiusByClass
} = {}) {
  const battleInputController = useMemo(() => createBattleInputController({
    open,
    canvasRef: glCanvasRef,
    runtimeRef,
    cameraControllerRef: cameraRef,
    cameraViewRectRef,
    worldToScreenRef,
    pointerWorldRef,
    panDragRef,
    deployYawDragRef,
    deployRectDragRef,
    spacePressedRef,
    constants: {
      ORDER_MOVE,
      CAMERA_ZOOM_STEP,
      CAMERA_DISTANCE_MIN,
      CAMERA_DISTANCE_MAX,
      DEPLOY_ROTATE_SENSITIVITY,
      DEPLOY_ROTATE_CLICK_THRESHOLD,
      DEPLOY_PITCH_DEG,
      BATTLE_UI_MODE_NONE,
      BATTLE_UI_MODE_PATH,
      BATTLE_UI_MODE_MARCH_PICK,
      BATTLE_UI_MODE_SKILL_PICK,
      BATTLE_UI_MODE_SKILL_CONFIRM,
      skillRangeByClass,
      skillAoeRadiusByClass
    },
    getters: {
      getSelectedSquadId: () => selectedSquadId,
      getBattleUiMode: () => battleUiMode,
      getSkillConfirmState: () => skillConfirmState,
      getAimState: () => aimState,
      getDeployDraggingGroupId: () => deployDraggingGroupId,
      getDeployDraggingTeam: () => deployDraggingTeam,
      getSelectedPaletteItemId: () => selectedPaletteItemId,
      isTrainingMode: () => isTrainingMode
    },
    callbacks: {
      setIsPanning,
      setCards,
      setMinimapSnapshot,
      setDeployNotice,
      setSelectedSquadId,
      setDeployDraggingGroup,
      setDeployActionAnchorMode,
      setPendingPathPoints,
      setPlanningHoverPoint,
      setBattleUiMode,
      setSkillPopupSquadId,
      setClockPaused,
      setAimState,
      setSkillConfirmState,
      setWorldActionsVisibleForSquadId,
      resolveDeployPlacementTeam,
      switchDeployGroupTeamForTraining,
      isPathPointBlocked,
      syncBattleCards,
      selectBattleSquad,
      closeSkillConfirm,
      closeSkillPick,
      closeMarchModePick,
      recallDeployDraggingGroup
    }
  }), [
    open,
    runtimeRef,
    cameraViewRectRef,
    worldToScreenRef,
    selectedSquadId,
    battleUiMode,
    skillConfirmState,
    aimState,
    deployDraggingGroupId,
    deployDraggingTeam,
    selectedPaletteItemId,
    isTrainingMode,
    resolveDeployPlacementTeam,
    switchDeployGroupTeamForTraining,
    isPathPointBlocked,
    syncBattleCards,
    selectBattleSquad,
    closeSkillConfirm,
    closeSkillPick,
    closeMarchModePick,
    recallDeployDraggingGroup,
    setClockPaused,
    setCards,
    setMinimapSnapshot,
    glCanvasRef,
    cameraRef,
    pointerWorldRef,
    panDragRef,
    deployYawDragRef,
    deployRectDragRef,
    spacePressedRef,
    setIsPanning,
    setDeployNotice,
    setSelectedSquadId,
    setDeployDraggingGroup,
    setDeployActionAnchorMode,
    setPendingPathPoints,
    setPlanningHoverPoint,
    setBattleUiMode,
    setSkillPopupSquadId,
    setAimState,
    setSkillConfirmState,
    setWorldActionsVisibleForSquadId,
    ORDER_MOVE,
    CAMERA_ZOOM_STEP,
    CAMERA_DISTANCE_MIN,
    CAMERA_DISTANCE_MAX,
    DEPLOY_ROTATE_SENSITIVITY,
    DEPLOY_ROTATE_CLICK_THRESHOLD,
    DEPLOY_PITCH_DEG,
    BATTLE_UI_MODE_NONE,
    BATTLE_UI_MODE_PATH,
    BATTLE_UI_MODE_MARCH_PICK,
    BATTLE_UI_MODE_SKILL_PICK,
    BATTLE_UI_MODE_SKILL_CONFIRM,
    skillRangeByClass,
    skillAoeRadiusByClass
  ]);

  useEffect(() => {
    if (!open) return undefined;
    return battleInputController.bindWindow();
  }, [open, battleInputController]);

  return {
    onMouseDown: battleInputController.onMouseDown,
    onMouseMove: battleInputController.onMouseMove,
    onWheel: battleInputController.onWheel,
    onContextMenu: battleInputController.onContextMenu,
    onMinimapClick: battleInputController.onMinimapClick
  };
}
